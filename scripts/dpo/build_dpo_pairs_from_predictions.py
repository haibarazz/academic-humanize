"""
Build SPIN-style DPO pairs from SFT model predictions.

For each train sample:
  prompt   = instruction + input
  chosen   = human reference from final_train_v2.json
  rejected = current best SFT LoRA prediction for the same input

This keeps the final AH validation set held out. Use a prediction report
generated by evaluation/predict/predict_local_model.py on the train split.
"""

from __future__ import annotations

import argparse
import json
import random
import re
from collections import Counter, defaultdict
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, Iterable, List, Mapping, Tuple


DEFAULT_TRAIN_FILE = "cloud_data/ah_v2/train/final_train_v2.json"
DEFAULT_OUTPUT_ALL = "cloud_data/ah_v2/dpo/ah_dpo_pairs_all.jsonl"
DEFAULT_OUTPUT_TRAIN = "cloud_data/ah_v2/dpo/train/ah_dpo_pairs_train.jsonl"
DEFAULT_OUTPUT_VAL = "cloud_data/ah_v2/dpo/val/ah_dpo_pairs_val.jsonl"
DEFAULT_REPORT_FILE = "cloud_data/ah_v2/dpo/ah_dpo_pairs_report.json"
DEFAULT_DATASET_VERSION = "ah_dpo_v2_spin"

CJK_RE = re.compile(r"[\u3400-\u9fff]")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build SPIN-style AH DPO pairs from SFT predictions")
    parser.add_argument("--train-file", type=str, default=DEFAULT_TRAIN_FILE)
    parser.add_argument("--prediction-report", type=str, required=True)
    parser.add_argument("--output-all", type=str, default=DEFAULT_OUTPUT_ALL)
    parser.add_argument("--output-train", type=str, default=DEFAULT_OUTPUT_TRAIN)
    parser.add_argument("--output-val", type=str, default=DEFAULT_OUTPUT_VAL)
    parser.add_argument("--report-file", type=str, default=DEFAULT_REPORT_FILE)
    parser.add_argument("--dataset-version", type=str, default=DEFAULT_DATASET_VERSION)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--val-ratio", type=float, default=0.10)
    parser.add_argument("--max-samples", type=int, default=0, help="0 means all prediction rows")
    parser.add_argument("--min-len-ratio", type=float, default=0.45)
    parser.add_argument("--max-len-ratio", type=float, default=2.20)
    parser.add_argument("--allow-missing-train-row", action="store_true")
    return parser.parse_args()


def read_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def write_json(path: Path, payload: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_suffix(path.suffix + ".tmp")
    with tmp_path.open("w", encoding="utf-8") as f:
        json.dump(dict(payload), f, ensure_ascii=False, indent=2)
    tmp_path.replace(path)


def write_jsonl(path: Path, rows: Iterable[Mapping[str, Any]]) -> int:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_suffix(path.suffix + ".tmp")
    count = 0
    with tmp_path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(dict(row), ensure_ascii=False) + "\n")
            count += 1
    tmp_path.replace(path)
    return count


def normalize_text(text: str) -> str:
    return re.sub(r"\s+", " ", (text or "").strip().lower())


def looks_english(text: str) -> bool:
    raw = (text or "").strip()
    if not raw or CJK_RE.search(raw):
        return False
    alpha = sum(1 for ch in raw if ch.isalpha())
    ascii_alpha = sum(1 for ch in raw if ch.isascii() and ch.isalpha())
    return alpha > 0 and ascii_alpha / max(alpha, 1) >= 0.60


def prompt_text(instruction: str, input_text: str) -> str:
    instruction = (instruction or "").strip()
    input_text = (input_text or "").strip()
    if input_text:
        return f"{instruction}\n\n{input_text}"
    return instruction


def load_prediction_rows(path: Path) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    payload = read_json(path)
    if not isinstance(payload, dict):
        raise ValueError(f"prediction report must be a JSON object: {path}")
    rows = payload.get("rows")
    if not isinstance(rows, list):
        raise ValueError(f"prediction report missing rows list: {path}")
    return payload, [row for row in rows if isinstance(row, dict)]


def validate_pair(pair: Mapping[str, Any], args: argparse.Namespace) -> Tuple[bool, str]:
    prompt = str(pair.get("prompt", "")).strip()
    chosen = str(pair.get("chosen", "")).strip()
    rejected = str(pair.get("rejected", "")).strip()
    if not prompt or not chosen or not rejected:
        return False, "empty_prompt_chosen_or_rejected"
    if rejected.startswith("ERROR:"):
        return False, "prediction_error"
    if not looks_english(chosen) or not looks_english(rejected):
        return False, "not_english"
    if normalize_text(chosen) == normalize_text(rejected):
        return False, "chosen_equals_rejected"
    length_ratio = len(rejected) / max(len(chosen), 1)
    if not (args.min_len_ratio <= length_ratio <= args.max_len_ratio):
        return False, "length_ratio_out_of_range"
    return True, ""


def build_pair(
    train_row: Mapping[str, Any],
    prediction_row: Mapping[str, Any],
    prediction_report: Mapping[str, Any],
    args: argparse.Namespace,
    row_index: int,
) -> Dict[str, Any]:
    sample_id = str(train_row.get("sample_id") or prediction_row.get("sample_id") or f"row_{row_index}").strip()
    instruction = str(train_row.get("instruction") or prediction_row.get("instruction") or "").strip()
    input_text = str(train_row.get("input") or prediction_row.get("input") or "").strip()
    chosen = str(train_row.get("output") or prediction_row.get("reference") or "").strip()
    rejected = str(prediction_row.get("prediction", "")).strip()
    length_ratio = len(rejected) / max(len(chosen), 1)
    return {
        "pair_id": f"{sample_id}__spin_sft_prediction",
        "sample_id": sample_id,
        "paper_id": str(train_row.get("paper_id") or prediction_row.get("paper_id") or "unknown").strip() or "unknown",
        "prompt": prompt_text(instruction, input_text),
        "chosen": chosen,
        "rejected": rejected,
        "metadata": {
            "pair_type": "spin_sft_prediction",
            "dataset_version": args.dataset_version,
            "instruction": instruction,
            "input_text": input_text,
            "source_candidate_id": str(train_row.get("candidate_id", "")).strip(),
            "source_dataset_version": str(train_row.get("dataset_version", "")).strip(),
            "prediction_model_id": str(prediction_report.get("model_id", "")).strip(),
            "prediction_report_type": str(prediction_report.get("report_type", "")).strip(),
            "prediction_mode": str(prediction_report.get("mode", "")).strip(),
            "prediction_run_id": str(prediction_report.get("run_id", "")).strip(),
            "prediction_row_index": row_index,
            "length_ratio_rejected_to_chosen": float(length_ratio),
        },
    }


def split_by_paper(rows: List[Dict[str, Any]], val_ratio: float, seed: int) -> Tuple[List[Dict[str, Any]], List[Dict[str, Any]]]:
    if not rows:
        return [], []
    paper_to_rows: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    for row in rows:
        paper_id = str(row.get("paper_id", "unknown")).strip() or "unknown"
        paper_to_rows[paper_id].append(row)

    paper_ids = sorted(paper_to_rows)
    rnd = random.Random(seed)
    rnd.shuffle(paper_ids)

    target_val = int(round(len(rows) * max(0.0, min(float(val_ratio), 0.5))))
    if target_val <= 0 and len(rows) > 1:
        target_val = 1

    train_rows: List[Dict[str, Any]] = []
    val_rows: List[Dict[str, Any]] = []
    val_count = 0
    for paper_id in paper_ids:
        group = paper_to_rows[paper_id]
        if val_count < target_val:
            val_rows.extend(group)
            val_count += len(group)
        else:
            train_rows.extend(group)

    if not train_rows and val_rows:
        train_rows.append(val_rows.pop())
    return train_rows, val_rows


def count_by(rows: Iterable[Mapping[str, Any]], key_path: Tuple[str, ...]) -> Dict[str, int]:
    counts: Counter[str] = Counter()
    for row in rows:
        value: Any = row
        for key in key_path:
            if isinstance(value, Mapping):
                value = value.get(key)
            else:
                value = None
                break
        counts[str(value or "unknown")] += 1
    return dict(counts)


def main() -> int:
    args = parse_args()
    train_file = Path(args.train_file)
    prediction_report_path = Path(args.prediction_report)

    train_rows_raw = read_json(train_file)
    if not isinstance(train_rows_raw, list):
        raise ValueError(f"train file must be a JSON list: {train_file}")
    train_by_sample_id = {
        str(row.get("sample_id", "")).strip(): row
        for row in train_rows_raw
        if isinstance(row, dict) and str(row.get("sample_id", "")).strip()
    }

    prediction_report, prediction_rows = load_prediction_rows(prediction_report_path)
    if args.max_samples and args.max_samples > 0:
        prediction_rows = prediction_rows[: args.max_samples]

    pairs: List[Dict[str, Any]] = []
    reject_counter: Counter[str] = Counter()
    seen_sample_ids = set()

    for row_index, pred_row in enumerate(prediction_rows, start=1):
        sample_id = str(pred_row.get("sample_id", "")).strip()
        if not sample_id:
            reject_counter["missing_sample_id"] += 1
            continue
        if sample_id in seen_sample_ids:
            reject_counter["duplicate_prediction_sample_id"] += 1
            continue
        seen_sample_ids.add(sample_id)

        train_row = train_by_sample_id.get(sample_id)
        if train_row is None:
            if not args.allow_missing_train_row:
                reject_counter["missing_train_row"] += 1
                continue
            train_row = pred_row

        pair = build_pair(
            train_row=train_row,
            prediction_row=pred_row,
            prediction_report=prediction_report,
            args=args,
            row_index=row_index,
        )
        ok, reason = validate_pair(pair, args)
        if not ok:
            reject_counter[reason] += 1
            continue
        pairs.append(pair)

    train_pairs, val_pairs = split_by_paper(pairs, args.val_ratio, args.seed)

    output_all = Path(args.output_all)
    output_train = Path(args.output_train)
    output_val = Path(args.output_val)
    report_file = Path(args.report_file)

    write_jsonl(output_all, pairs)
    write_jsonl(output_train, train_pairs)
    write_jsonl(output_val, val_pairs)

    train_papers = {str(row.get("paper_id", "")) for row in train_pairs}
    val_papers = {str(row.get("paper_id", "")) for row in val_pairs}
    report = {
        "report_type": "ah_dpo_spin_pair_build_report",
        "created_at": datetime.now().isoformat(),
        "dataset_version": args.dataset_version,
        "inputs": {
            "train_file": str(train_file),
            "prediction_report": str(prediction_report_path),
        },
        "prediction_report": {
            "model_id": str(prediction_report.get("model_id", "")).strip(),
            "mode": str(prediction_report.get("mode", "")).strip(),
            "run_id": str(prediction_report.get("run_id", "")).strip(),
            "settings": prediction_report.get("settings", {}),
        },
        "outputs": {
            "all_pairs": str(output_all),
            "train_pairs": str(output_train),
            "val_pairs": str(output_val),
        },
        "counts": {
            "train_source_rows": len(train_rows_raw),
            "prediction_rows": len(prediction_rows),
            "all_pairs": len(pairs),
            "train_pairs": len(train_pairs),
            "val_pairs": len(val_pairs),
            "train_papers": len(train_papers),
            "val_papers": len(val_papers),
            "paper_overlap": len(train_papers & val_papers),
        },
        "distribution": {
            "pair_type": count_by(pairs, ("metadata", "pair_type")),
        },
        "rejections": dict(reject_counter),
        "config": {
            "seed": args.seed,
            "val_ratio": args.val_ratio,
            "max_samples": args.max_samples,
            "min_len_ratio": args.min_len_ratio,
            "max_len_ratio": args.max_len_ratio,
            "allow_missing_train_row": args.allow_missing_train_row,
        },
        "leakage_note": (
            "Build predictions from cloud_data/ah_v2/train/final_train_v2.json only. "
            "Do not generate DPO pairs from cloud_data/ah_v2/val/final_val_v2.json."
        ),
    }
    write_json(report_file, report)

    print("=" * 72)
    print("Academic Humanize SPIN-style DPO pair build complete")
    print("=" * 72)
    print(f"prediction_report: {prediction_report_path}")
    print(f"all_pairs:         {len(pairs)}")
    print(f"train_pairs:       {len(train_pairs)}")
    print(f"val_pairs:         {len(val_pairs)}")
    print(f"paper_overlap:     {len(train_papers & val_papers)}")
    print(f"output_train:      {output_train}")
    print(f"output_val:        {output_val}")
    print(f"report_file:       {report_file}")
    print("=" * 72)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

